#classes e objetos 

#classes são as formas para gerar objetos, como se fosse uma plata de uma casa

#objeto é o objeto propriamente pronto, uma casa pronta que voce pode utilizar para fazer outras casas mudando caracteristicas

#classes são a maneira que temos de informar como queremos que nosso objeto funcione
#ao criarmos uma classe podemos especificar os metodos e os atriutos que os ojetos possuirão, exemplo um carro: cor, motor, modelo, placa

#os métodos são as ações.
#os atributos são as caracteristicas

#métodos do carro: andar pra frente, pra tras, trocar de marcha, acender farois
#atributos do carro: cor, modelo, placa, aro da roda, cor do banco, etc


