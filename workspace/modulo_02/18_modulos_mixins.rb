#módulos Ruby
#são similares a classes em relação ao fato de que também armazenam uma
# coleção de métodos, constantes e outras definições de módulos e classes.
    
#ENTRETANTO, diferente das classes, você não pode criar objetos baseados em
 #módulos nem pode criar módulos que herdam desse módulo; ao invés disso,
  #você especifica qual a funcionalidade de um módulo específico você deseja
   #adicionar a uma classe ou a um objeto específico.

#módulos permanecem sozinhos; não há hierarquia de módulos ou herança, módulos
 #são um bom lugar para armazenar constantes em um local centralizado.
 
#PRINCIPAIS OBJETIVOS:

    #1. Agem como namespace, permitindo que voce defina métodos cujo nome não 
    #irão colidir com aqueles definidos em outras partes de um programa.
    
    #2. Permite que voce compartilhe funcionalidade entre classes - se uma
    #classe "mistura" um módulo, todos os métodos de instancia do módulo
    #se tornam disponíveis como se tivessem sido definidos na classe
    
